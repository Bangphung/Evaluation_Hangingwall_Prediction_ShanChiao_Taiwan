% Rrup   = Closest distance (km) to the ruptured plane
% SOE    =  type of event
%        = 0 interface
%        = 1 intra slab
% flag       = 0 for Japan+Taiwan
%            = 1 for Taiwan
% Vs30          = shear wave velocity averaged over top 30 m in m/s

% Output Variables
% Sa: Median spectral acceleration prediction
% sigma: logarithmic standard deviation of spectral acceleration
%          prediction
% tau
% phi
function [Sa,  sigma, phiS2S] = Phung_2020_TWJP_SDZ(T,M,Ztor,Rrup,Vs30,Z10,flag,eqt)
    period = [0 0.01 0.02  0.05  0.075  0.1  0.15  0.20  0.25  0.3  0.4  0.5  0.6  0.75  ... 
              1  1.5  2  2.5  3  4  5, -1];
    % ip = find(period==T);
    NT=length(T);
    Sa=zeros(1,NT);
    sigma=zeros(1,NT);
    phiS2S=zeros(1,NT);

for I=1:1:NT
    T_low = max(period(period <= T(I)));
    T_high = min(period(period >= T(I)));
    ip_low  = find(period==T_low);
    ip_high = find(period==T_high);
    if ( ip_low == ip_high )
        [Sa(I),sigma(I),phiS2S(I)]= PLC_SDZ17_sub(M,Ztor,Rrup,Vs30,Z10,ip_low,flag,eqt);     
    else
        [Sa_low,sigma_low,phis2s_low] = PLC_SDZ17_sub(M,Ztor,Rrup,Vs30,Z10,ip_low,flag,eqt);
        [Sa_high,sigma_high,phis2s_high] = PLC_SDZ17_sub(M,Ztor,Rrup,Vs30,Z10,ip_high,flag,eqt);
        
        x = [log(T_low) log(T_high)];
        Y_sa = [log(Sa_low) log(Sa_high)];        
        Y_sigma = [sigma_low sigma_high];
        Y_phis2s = [phis2s_low phis2s_high];
       
        Sa(I) = exp(interp1(x, Y_sa, log(T(I))));
        sigma(I) = interp1(x, Y_sigma, log(T(I)));
        phiS2S(I) = interp1(x, Y_phis2s, log(T(I)));
         
    end
end


