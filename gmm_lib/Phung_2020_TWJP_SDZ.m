function PGA_r = pred_PGA_r(M, Rrup, Vs30, Ztor, eqt, flag, C)
       ip = 1; % ~ T=0 or PGA
    %% Regional term
    if flag == 0
       a1 = C.a1(ip) + C.del_a1(ip);
       a4 = C.a4(ip) + C.del_a4(ip);
       a6 = C.a6_jp(ip);
       a12 = C.Si12jp(ip);
    else
       a1 = C.a1(ip);
       a4 = C.a4(ip);
       a6 = C.a6(ip);
       a12 = C.Si12tw(ip);
     
    end
    %% Magnitude Scaling
    if M <= C.Mref(ip)
       fmag =  a4*(M - C.Mref(ip)) + C.a13(ip)*(10-M)^2;
    else
       fmag =  C.a5(ip)*(M - C.Mref(ip)) + C.a13(ip)*(10-M)^2;
    end  
    %% FZtor
    if eqt ==0 
       f_ztor = C.a10(ip)*(min(Ztor,40)-20);
    else
       f_ztor = C.a11(ip)*(min(Ztor,80)-40); 
    end
    %% Path Scaling
    X = a1 + C.a7(ip)*(eqt==1) + (C.a2(ip) + C.a14(ip)*(eqt==1) + C.a3*(M -7.8))*log(Rrup + C.c4*exp(C.a9*(M-6)))+ a6*Rrup;
    %% VS30 Scaling (Site Effect)
    Vs30 = min(Vs30,1000);
    fsite =  a12*log(Vs30/C.Vlin(1))+ C.b(ip)*C.n*log(Vs30/C.Vlin(ip));
    
    %% Median Prediction Model
    PGA_r = exp(fmag + X  + f_ztor + fsite);
end
